import Foundation
extension UIFont {
    //通用字体、颜色
    static let mainName = "PingFangTC-Thin"
    static let tiny = UIFont(name: UIFont.mainName, size: 9)!
    static let small = UIFont(name: UIFont.mainName, size: 12)!
    static let middle = UIFont(name: UIFont.mainName, size: 17)!
    static let big = UIFont(name: UIFont.mainName, size: 24)!
    static let huge = UIFont(name: UIFont.mainName, size: 44)!
}
